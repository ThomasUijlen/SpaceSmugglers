	public ShipPattern(SpaceSmugglers p, int ship) {
		this.app = p;
		this.ship = ship;
	}
	
	public void loadPattern(boolean unlocked) {
		int[][] pattern = this.pattern[ship];
		for(int i = 0; i < pattern.length; i++) {
			int x = pattern[i][0] + app.width/2;
			int y = pattern[i][1] + app.height/2;
			Sprite sp = new Sprite("src/main/data/textures/blueSlot.png");
			BlueprintSlot s = new BlueprintSlot(app, sp, unlocked);
    		app.addGameObject(s, x, y);
   
    		
    		if(pattern[i][0] == -50 && pattern[i][1] == 0
    				|| pattern[i][0] == 50 && pattern[i][1] == 0
    				|| pattern[i][0] == 0 && pattern[i][1] == -50
    				|| pattern[i][0] == 0 && pattern[i][1] == 50) {
    			s.fillSlot();
    		}
    		s.checkStatus();
		}
		
		Sprite sp = new Sprite("src/main/data/textures/ShipCore.png");
		ShipCore c = new ShipCore(app, sp, 100, 50, 100);
		app.addGameObject(c, app.width/2, app.height/2);
	}
